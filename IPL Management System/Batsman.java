public class Batsman {
    private int runs;
    private int hundreds;
    private int fiftys;

    public Batsman(int runs, int hundreds, int fiftys) {
        this.runs = runs;
        this.hundreds = hundreds;
        this.fiftys = fiftys;
    }

    public int getRuns() {
        return runs;
    }

    public void setRuns(int runs) {
        this.runs = runs;
    }

    public int getHundreds() {
        return hundreds;
    }

    public void setHundreds(int hundreds) {
        this.hundreds = hundreds;
    }

    public int getFiftys() {
        return fiftys;
    }

    public void setFiftys(int fiftys) {
        this.fiftys = fiftys;
    }


    
}
